public class Functions {
	
	//'+'43
public float add(int num1, int num2) {

	float ret = num1 + num2;
	return ret;
}

	//'-'45
public float mins(int num1, int num2) {
	float ret = num1 - num2;
	return ret;
}

	//'*'42
public float multi(int num1, int num2) {
	float ret = num1 * num2;
	return ret;
}

	//'/'47
public float devide(int num1, int num2) {
	float ret = num1 / num2;
	return ret;
}

}
